// ─── Main ──────────────────────────────────────────────────
function main() {
  console.log(`New scholarships to add: ${newScholarships.length}`);

  const dbRaw = readFileSync(DB_PATH, 'utf-8');
  const db = JSON.parse(dbRaw);

  const existingNames = new Set((db.scholarships || []).map(s => s.name.toLowerCase()));

  let added = 0;
  let skipped = 0;

  for (const schol of newScholarships) {
    if (existingNames.has(schol.name.toLowerCase())) {
      skipped++;
      continue;
    }
    db.scholarships.push(schol);
    existingNames.add(schol.name.toLowerCase());
    added++;
  }

  writeFileSync(DB_PATH, JSON.stringify(db, null, 2), 'utf-8');

  console.log(`\n📊 Summary:`);
  console.log(`  Added: ${added}`);
  console.log(`  Skipped (duplicates): ${skipped}`);
  console.log(`  Total in db.json: ${db.scholarships.length}`);
}

main();
