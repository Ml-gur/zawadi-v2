function schol({
  name, provider, host, host_institution, description, eligibility, amount, deadline,
  apply_url, source_url, degree_levels, fields_of_study, countries, funding_type,
  no_ielts, work_experience_required, age_limit_masters, age_limit_phd,
  host_region, host_country, iso2, required_documents,
  sponsor_type, pipeline_source, quality_score, scam_flags, urgency,
  confidence
}) {
  return {
    id: crypto.createHash('md5').update(name + provider + (deadline || '')).digest('hex').slice(0, 12),
    name, provider, host, host_institution: host_institution || null,
    description: description || null,
    eligibility: eligibility || null,
    amount: amount || null,
    deadline: deadline || null,
    apply_url: apply_url || null,
    source_url: source_url || apply_url || '',
    degree_levels: degree_levels || [],
    fields: fields_of_study || [],
    fields_of_study: fields_of_study || [],
    countries: countries || ['All African Countries'],
    funding_type: funding_type || null,
    required_documents: required_documents || [],
    no_ielts: no_ielts ?? null,
    work_experience_required: work_experience_required ?? null,
    age_limit_masters: age_limit_masters ?? null,
    age_limit_phd: age_limit_phd ?? null,
    host_region: host_region || null,
    host_country: host_country || null,
    iso2: iso2 || null,
    published: false,
    sponsor_type: sponsor_type || null,
    pipeline_source: pipeline_source || 'manual_import',
    quality_score: quality_score ?? null,
    scam_flags: scam_flags || [],
    urgency: urgency || 'Normal',
    view_count: 0,
  };
}

