// ─── Main: Update db.json ──────────────────────────────────
function main() {
  console.log(`Total scholarships in data: ${allScholarships.length}`);

  // Read current db.json
  const dbRaw = readFileSync(DB_PATH, 'utf-8');
  const db = JSON.parse(dbRaw);

  // Get existing scholarship names to skip duplicates
  const existingNames = new Set((db.scholarships || []).map(s => s.name.toLowerCase()));

  let added = 0;
  let skipped = 0;

  for (const schol of allScholarships) {
    if (existingNames.has(schol.name.toLowerCase())) {
      skipped++;
      continue;
    }
    db.scholarships.push(schol);
    existingNames.add(schol.name.toLowerCase());
    added++;
  }

  writeFileSync(DB_PATH, JSON.stringify(db, null, 2), 'utf-8');

  console.log(`\n📊 Summary:`);
  console.log(`  Added: ${added}`);
  console.log(`  Skipped (duplicates): ${skipped}`);
  console.log(`  Total in db.json: ${db.scholarships.length}`);
  console.log(`\n  Archived (published: false): ${allScholarships.filter(s => s.urgency === 'Archived').length}`);
  console.log(`  Live (published: false, pending review): ${allScholarships.filter(s => s.urgency !== 'Archived').length}`);
}

main();
